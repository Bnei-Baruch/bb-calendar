export function getEventsByDate(date: string): Event[] {
  return events.filter(e => e.date === date).sort((a, b) => 
    a.startTime.localeCompare(b.startTime)
  );
}

export function getEventById(id: string): Event | undefined {
  return events.find(e => e.id === id);
}

export function getMonthEvents(year: number, month: number): Map<string, Event[]> {
  const monthStr = String(month).padStart(2, '0');
  const prefix = `${year}-${monthStr}`;
  
  const monthEvents = events.filter(e => e.date.startsWith(prefix));
  const eventsByDay = new Map<string, Event[]>();
  
  monthEvents.forEach(event => {
    const existing = eventsByDay.get(event.date) || [];
    eventsByDay.set(event.date, [...existing, event]);
  });
  
  return eventsByDay;
}